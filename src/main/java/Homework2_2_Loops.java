public class Homework2_2_Loops {
    public static void main(String[] args) {
//calculate summary

        int a = 1;
        int b = 3;
        int sum = 0;

        for (int i = a; i <= b; i++) {
            sum = sum + i;

        }
        System.out.println("The summary of 2 numbers : " + a + " and " + b + " is " + sum);

    }


}
